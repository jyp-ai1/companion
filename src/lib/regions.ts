export const SIDO_LIST = Object.keys(REGION_DATA);

export function formatRegion(sido: string, sigungu: string) {
  return `${sido} ${sigungu}`;
}

export function parseRegion(region: string | null): { sido: string; sigungu: string } {
  if (!region) return { sido: "", sigungu: "" };
  for (const sido of SIDO_LIST) {
    if (region.startsWith(sido)) {
      return { sido, sigungu: region.slice(sido.length).trim() };
    }
  }
  const parts = region.split(" ");
  return { sido: parts[0] ?? "", sigungu: parts.slice(1).join(" ") };
}
